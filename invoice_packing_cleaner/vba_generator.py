def _find_rule(rules: list[SheetTransformRule], section: str) -> SheetTransformRule | None:
    section = section.upper()
    for rule in rules:
        haystack = " ".join(
            [
                rule.procedure_name,
                rule.source_sheet_name,
                rule.output_sheet_name,
            ]
        ).upper()
        if section in haystack:
            return rule
    return None


def _vba_string(value: object) -> str:
    return '"' + str(value).replace('"', '""') + '"'


def _vba_boolean(value: bool) -> str:
    return "True" if value else "False"


def _vba_array_strings(values: list[str]) -> str:
    return "Array(" + ", ".join(_vba_string(value) for value in values) + ")"


def _vba_array_numbers(values: list[int]) -> str:
    return "Array(" + ", ".join(str(int(value)) for value in values) + ")"


def _strict_col(rule: SheetTransformRule | None, *keywords: str) -> int:
    if rule is None:
        return 0

    normalized_keywords = [keyword.casefold() for keyword in keywords]
    for mapping in rule.mappings:
        haystack = f"{mapping.target} {mapping.source_header}".casefold()
        if any(keyword in haystack for keyword in normalized_keywords):
            return int(mapping.source_index or 0)
    return 0


